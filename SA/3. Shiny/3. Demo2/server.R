shinyServer(
  function(input,output){
    
    output$describe <- renderPrint({
      data = subset(iris,Species==input$Species)
      describe(data[,sapply(data,is.numeric)])
})
      output$cor <- renderPrint({
        data = subset(iris,Species==input$Species)
        cor(data[,sapply(data,is.numeric)])
        
    })
  }
)

